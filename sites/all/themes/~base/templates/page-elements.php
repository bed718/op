<h1>About the Chefs</h1>
<div class="side-left">
	<h2>Karen Hatfield</h2>
	<h3>Owner/Pastry Chef</h3>
	<p>Karen Hatfield’s pastry career began not far from home at the Los Angeles Culinary Institute, where she perfected her innate ability to take classic combinations and translate them in unexpected ways, a signature of Hatfield’s today. Upon graduation, Hatfield served as a recipe tester, followed by a pastry chef position at the venerable Spago Beverly Hills and Spago Hollywood, where she met her husband, Quinn. A few years later, the two moved to New York and quickly immersed themselves in the New York restaurant scene. Karen held positions at Café Boulud, Jean-Georges and Gramercy Tavern, where she worked alongside James Beard Award winner Claudia Fleming. In 2002, Karen and Quinn moved to San Francisco to open Cortez restaurant in Union squares hotel Adagio where Karen also managed the front of the house. San Francisco was exciting and the restaurant was flourishing but after three years the Hatfields longed for their own restaurant back home in L.A. In early 2006 the chefs discovered a quaint restaurant space on Beverly Boulevard and quickly began work on their labor of love. In it’s original incarnation Hatfield’s was named best new restaurant by Los Angeles Magazine and garnered a Michelin star. Three years later, the culinary couple have re-opened in a larger space on Melrose Avenue, where they continue to execute their critically-acclaimed New American cuisine earning accolades such as Bon Appetit’s 10 Best New Restaurants in the country, and earning Los Angeles Magazine’s top rating of Four Stars.</p>
</div>
<div class="side-right">
	<h2>Quinn Hatfield</h2>
	<h3>Owner/Chef</h3>
	<p>Quinn Hatfield discovered his culinary passion at an early age, having spent much of his childhood in the kitchens and dining rooms of Splendido’s and Wolfgang Puck’s Postrio in San Francisco. Eventually settling in Los Angeles in 1997, the North Carolina native worked as Sous Chef of Spago Hollywood, where he met his wife, Karen. Shortly after, he was named Chef de Partie at Spago Beverly Hills working alongside esteemed chef Lee Hefter, before relocating to New York with Karen. Back east, Hatfield’s career led him to a number of esteemed restaurants, including Rocco DiSpirito’s Union Pacific, Jean-Georges and Bouley’s kitchen, where Hatfield thrived in the competitive restaurant scene. In 2002, the dynamic duo helmed their first restaurant working side-by-side with Quinn as Executive Chef of Cortez, a restaurant located in Union Square’s Hotel Adagio. Quinn and Karen earned three stars and were named rising star chefs by the San Francisco Chronicle. Leading his own kitchen for the first time reinvigorated Quinn’s dream of opening his own restaurant. During a scouting trip to Los Angeles, the Hatfields uncovered a quaint space on Beverly Boulevard and the first inception of Hatfield’s restaurant debuted in 2006. A true labor of love, Hatfield’s flourished in the 40-seat space and the couple decided to fulfill their dreams of owning a larger restaurant. This new incarnation of Hatfield’s restaurant on Melrose Avenue opened in February 2010 to great acclaim, including accolades such as “Best New Restaurant” 2010 by Angeleno Magazine and a three-star review from The Los Angeles Times.</p>
</div>



